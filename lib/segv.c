#include "lib.h"

void lib_segv(int signum, siginfo_t *info, void *ptr)
{
    lib_backtrace();
    hvfs_info(lib, "SIGSEGV info: signo %d errno %d code %d\n",
              info->si_signo, info->si_errno, info->si_code);
    exit(EFAULT);
}
